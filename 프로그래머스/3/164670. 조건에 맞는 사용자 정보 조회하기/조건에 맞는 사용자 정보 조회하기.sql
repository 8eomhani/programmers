SELECT UGU.USER_ID, 
       UGU.NICKNAME, 
       CONCAT(UGU.CITY,' ', UGU.STREET_ADDRESS1,' ',UGU.STREET_ADDRESS2) AS 전체주소, 
       CONCAT(SUBSTR(UGU.TLNO,1,3),'-', SUBSTR(UGU.TLNO,4,4),'-', SUBSTR(UGU.TLNO,8,4)) AS 전화번호 
FROM USED_GOODS_BOARD UGB, USED_GOODS_USER UGU
WHERE UGB.WRITER_ID = UGU.USER_ID
GROUP BY UGU.USER_ID, UGU.NICKNAME
HAVING COUNT(UGU.USER_ID)>=3
ORDER BY UGU.USER_ID DESC;