-- EQUI JOIN
SELECT B.CATEGORY, SUM(BS.SALES) AS TOTAL_SALES
FROM BOOK B, BOOK_SALES BS
WHERE BS.SALES_DATE BETWEEN TO_DATE(20220101,'YYYY-MM-DD') AND TO_DATE(20220131, 'YYYY-MM-DD')
AND B.BOOK_ID = BS.BOOK_ID
GROUP BY B.CATEGORY

